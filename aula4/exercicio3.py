a = int(input("Digite um número até 7: "))

if a == 1:
    print("Domingo")
elif a == 2:
    print("Segunda")
elif a == 3:
    print("Terça")
elif a == 4:
    print("Quarta")
elif a == 5:
    print("Quinta")
elif a == 6:
    print("Sexta")
elif a == 7:
    print("Sábado")
else:
    print("Digite um número válido entre 1-7.")
